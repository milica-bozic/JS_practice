// 25. zadatak
/* Dati su nizovi 
a[0], a[1], ...,a[n - 1] i
b[0], b[1], ...,b[n - 1].
formirati niz c[0], c[1], ...,c[n - 1] ciji su elementi
a[0] * b[0], a[1] * b[1], ...,a[n - 1] * b[n - 1]
*/

let zad25 = (a, b) => {
    let n = a.length;
    let c = [];
    
    for (let i = 0; i < 0; i++) {
        c[i] = a[i] * b[i];
    }
    return c;
}
